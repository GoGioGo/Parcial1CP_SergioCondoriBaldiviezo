    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        String a=field.getText();
        String expresion[]=new String [a.length()];
        for (int i=0;i<a.length();i++)
        {
            expresion[i]=String.valueOf(a.charAt(i));
        }
        operaciones o=new operaciones(expresion);
        if(expresion[0].equals("+")||expresion[0].equals("-")||expresion[0].equals("/")||expresion[0].equals("*")||expresion[0].equals("^"))
        {
            label.setText("La expresion es PREFIJA");
            field.setText(String.valueOf(o.Prefija(expresion)));
            cadena="";
        }
        else
        {
            if(expresion[expresion.length-1].equals("+")||expresion[expresion.length-1].equals("-")||expresion[expresion.length-1].equals("/")||expresion[expresion.length-1].equals("*")||expresion[expresion.length-1].equals("^"))
            {
                label.setText("La expresion es POSTFIJA");
                field.setText(String.valueOf(o.Postfija(expresion)));
                cadena="";
            }
            else
            {
                label.setText("La expresion es INFIJA");
                field.setText(String.valueOf(o.Infija(expresion)));
                cadena="";
            }
        }
    }//GEN-LAST:event_jButton8ActionPerformed

